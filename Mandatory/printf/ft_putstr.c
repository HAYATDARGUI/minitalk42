#include "ft_printf.h"

void	ft_putstr(char *s, int *len)
{
	if (!s)
	{
		ft_putstr("(null)", len);
		return ;
	}
	while (*s)
	{
		write(1, s, 1);
		s++;
		*len += 1;
	}
}
